public class RoomHour {
    private int startHour;
    private int endHour;

    public int getStartHour() {
        return startHour;
    }

    public int getEndHour() {
        return endHour;
    }

    public RoomHour(int startHour, int endHour){
        this.startHour = startHour;
        this.endHour = endHour;
    }
}
